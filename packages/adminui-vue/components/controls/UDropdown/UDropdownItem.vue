<template>
  <div
    v-if="divider"
    class="u-dropdown-item__divider"
  />

  <u-dropdown
    v-else
    placement="right-start"
    position="absolute"
  >
    <button
      :disabled="disabled"
      class="u-dropdown-item"
      @click="close"
      v-on="$listeners"
    >
      <i
        v-if="icon"
        :class="icon"
        class="u-dropdown-item__icon"
      />

      <span class="u-dropdown-item__label">
        <!-- @slot Replace label prop -->
        <slot
          v-if="$slots.label"
          name="label"
        />

        <template v-else>
          {{ $ut(label) }}
        </template>
      </span>

      <i
        v-if="hasChildren"
        class="u-dropdown-item__arrow u-icon-arrow-right"
      />
    </button>

    <!-- @slot For children u-dropdown-item -->
    <slot slot="dropdown" />
  </u-dropdown>
</template>

<script>
/**
   * Child of UDropdown
   */
export default {
  name: 'UDropdownItem',

  props: {
    /**
       * Icon class
       */
    icon: String,

    /**
       * Item text
       */
    label: String,

    /**
       * Render divider ignore other props
       */
    divider: Boolean,

    /**
       * Disabled state
       */
    disabled: Boolean,

    /**
       * Prevent close dropdown
       */
    preventClose: Boolean
  },

  computed: {
    hasChildren () {
      return this.$slots.default !== undefined
    }
  },

  mounted () {
    this.$on('hide', () => {
      this.$parent.$emit('hide')
    })
  },

  inject: ['parentClose'],

  methods: {
    close () {
      if (this.preventClose || this.hasChildren) {
        return
      }
      this.parentClose()
    }
  }
}
</script>

